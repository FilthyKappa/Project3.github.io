document.getElementById("finalb").onclick= function(){
    let sr = document.getElementById("a1").value;
    sr = Number(sr);
    document.getElementById("a2").innerHTML = sr;
    let dm = document.getElementById("b1").value;
    dm = Number(dm);
    document.getElementById("b2").innerHTML = dm;
    let dl = document.getElementById("c1").value;
    dl = Number(dl);
    document.getElementById("c2").innerHTML = dl;
    let de = document.getElementById("d1").value;
    de = Number(de);
    document.getElementById("d2").innerHTML = de;
    let vmo = document.getElementById("e1").value;
    vmo = Number(vmo);
    document.getElementById("e2").innerHTML = vmo;
    let fmo = document.getElementById("f1").value;
    fmo = Number(fmo);
    document.getElementById("f2").innerHTML = fmo;
    let s1 = dm + dl + de + vmo + fmo;
    s1 = Number(s1);
    document.getElementById("a").innerHTML = s1;
    let os = document.getElementById("g1").value;
    os = Number(os);
    document.getElementById("g2").innerHTML = os;
    let cs = document.getElementById("h1").value;
    cs = Number(cs);
    document.getElementById("h2").innerHTML = cs;
    let s2 = s1 + os;
    s2 = Number(s2);
    let s3 = s2 - cs;
    s3 = Number(s3);
    document.getElementById("b").innerHTML = s3;
    let uafmo = document.getElementById("i1").value;
    uafmo = Number(uafmo);
    document.getElementById("i2").innerHTML = uafmo;
    let oafmo = document.getElementById("j1").value;
    oafmo = Number(oafmo);
    document.getElementById("j2").innerHTML = oafmo;
    let s4 = s3 + uafmo;
    s4 = Number(s4);
    let s5 = s4 - oafmo;
    s5 = Number(s5);
    document.getElementById("j3").innerHTML = s5;
    document.getElementById("gpm").innerHTML = sr - s5;
    let s6 = sr - s5;
    s6 = Number(s6);
    let vsdo = document.getElementById("k1").value;
    vsdo = Number(vsdo);
    document.getElementById("k2").innerHTML = vsdo;
    let voao = document.getElementById("l1").value;
    voao = Number(voao);
    document.getElementById("l2").innerHTML = voao;
    let fsdo = document.getElementById("m1").value;
    fsdo = Number(fsdo);
    document.getElementById("m2").innerHTML = fsdo;
    let foao = document.getElementById("n1").value;
    foao = Number(foao);
    document.getElementById("n2").innerHTML = foao;
    let s7 = vsdo + voao + fsdo + foao;
    s7 = Number(s7);
    document.getElementById("n3").innerHTML = s7;
    document.getElementById("ans").innerHTML = s6 - s7;

}